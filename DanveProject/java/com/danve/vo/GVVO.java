package com.danve.vo;

public class GVVO {
	private String v_idx, name, v_context, m_idx, writetime;

	public String getWritetime() {
		return writetime;
	}

	public void setWritetime(String writetime) {
		this.writetime = writetime;
	}

	public String getV_idx() {
		return v_idx;
	}

	public void setV_idx(String v_idx) {
		this.v_idx = v_idx;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getV_context() {
		return v_context;
	}

	public void setV_context(String v_context) {
		this.v_context = v_context;
	}

	public String getM_idx() {
		return m_idx;
	}

	public void setM_idx(String m_idx) {
		this.m_idx = m_idx;
	}
	
}
